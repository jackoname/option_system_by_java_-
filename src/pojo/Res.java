package pojo;

public class Res {
    int runtime,starttime,endtime;
    double roundtime,wroundtime;

    public int getRuntime() {
        return runtime;
    }

    public void setRuntime(int runtime) {
        this.runtime = runtime;
    }

    public int getStarttime() {
        return starttime;
    }

    public void setStarttime(int starttime) {
        this.starttime = starttime;
    }

    public int getEndtime() {
        return endtime;
    }

    public void setEndtime(int endtime) {
        this.endtime = endtime;
    }

    public double getRoundtime() {
        return roundtime;
    }

    public void setRoundtime(double roundtime) {
        this.roundtime = roundtime;
    }

    public double getWroundtime() {
        return wroundtime;
    }

    public void setWroundtime(double wroundtime) {
        this.wroundtime = wroundtime;
    }

    public void setWroundtime(int wroundtime) {
        this.wroundtime = wroundtime;
    }
}
